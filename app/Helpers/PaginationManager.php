<?php

namespace App\Helpers;


class PaginationManager
{
	public static function Pagination_Size($Page_Size)
	{
		//------------------------------------------------------------------------------
		//	VARIABLES
		//------------------------------------------------------------------------------
		$oResult = 0;


		//------------------------------------------------------------------------------
		//	FUNCTION
		//------------------------------------------------------------------------------
		if($Page_Size > (int)config("system.pagination.size.max"))
		{
			$oResult = (int)config("system.pagination.size.max");
		}
		else
		{
			$oResult = $Page_Size;
		}


		//------------------------------------------------------------------------------
		//	RESPONSE
		//------------------------------------------------------------------------------
		return $oResult;

	}
	public static function Pagination_Offset($Page_Size, $Page_Current)
	{
		//------------------------------------------------------------------------------
		//	VARIABLES
		//------------------------------------------------------------------------------
		$oResult 	= 0;
		$Page_Size 	= SELF::Pagination_Size($Page_Size);


		//------------------------------------------------------------------------------
		//	FUNCTION
		//------------------------------------------------------------------------------
		$oResult 	= $Page_Size * ( $Page_Current - 1 );


		//------------------------------------------------------------------------------
		//	RESPONSE
		//------------------------------------------------------------------------------
		return $oResult;

	}
}