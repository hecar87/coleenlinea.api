<?php

namespace App\Helpers;


class ResultManager
{
	public static function Result($Code, $Entity, $Data = null, $Data_Total = 0, $Error = "")
	{
		//------------------------------------------------------------------------------
		//	VARIABLES
		//------------------------------------------------------------------------------
		$oResult	= array();


		//------------------------------------------------------------------------------
		//	FUNCTION
		//------------------------------------------------------------------------------
		$oResult	= array(
			(object)[
				"RESULT_STS"	=> SELF::Result_Status($Code),
				"RESULT_COD"	=> SELF::Result_Code($Code),
				"RESULT_DOM"	=> SELF::Result_Domain($Entity),
				"RESULT_MSG"	=> SELF::Result_Message($Code),
				"RESULT_DTA"	=> $Data,
				"RESULT_DTL"	=> $Data_Total,
				"RESULT_ERR"	=> $Error
			]
		);


		//------------------------------------------------------------------------------
		//	RESPONSE
		//------------------------------------------------------------------------------
		return $oResult;

	}


	private static function Result_Status($Code)
	{
		//------------------------------------------------------------------------------
		//	VARIABLES
		//------------------------------------------------------------------------------
		$oMessage	= include(resource_path("responses/message.php"));
		$oResult	= 0;


		//------------------------------------------------------------------------------
		//	FUNCTION
		//------------------------------------------------------------------------------
		if ( array_key_exists($Code, $oMessage) )
		{
			$oResult = $oMessage[$Code]["Status"];
		}
		else
		{
			$oResult = $oMessage[9900]["Status"];
		}

		//------------------------------------------------------------------------------
		//	RESPONSE
		//------------------------------------------------------------------------------
		return $oResult;

	}
	private static function Result_Code($Code)
	{
		//------------------------------------------------------------------------------
		//	VARIABLES
		//------------------------------------------------------------------------------
		$oMessage	= include(resource_path("responses/message.php"));
		$oResult	= 0;


		//------------------------------------------------------------------------------
		//	FUNCTION
		//------------------------------------------------------------------------------
		if ( array_key_exists($Code, $oMessage) )
		{
			$oResult = $oMessage[$Code]["Code"];
		}
		else
		{
			$oResult = $oMessage[9900]["Code"];
		}


		//------------------------------------------------------------------------------
		//	RESPONSE
		//------------------------------------------------------------------------------
		return $oResult;

	}
	private static function Result_Domain($Entity)
	{
		//------------------------------------------------------------------------------
		//	VARIABLES
		//------------------------------------------------------------------------------
		$oResult	= "";
		$Prefix		= config("app.code");


		//------------------------------------------------------------------------------
		//	FUNCTION
		//------------------------------------------------------------------------------
		switch ($Entity)
		{
			case "none"		: $oResult = $Prefix . ".NE"; break;
			case "global"	: $oResult = $Prefix . ".GLOBAL"; break;
			case "server"	: $oResult = $Prefix . ".SERVER"; break;
			default			: $oResult = $Prefix . "." . strtoupper($Entity); break;
		}


		//------------------------------------------------------------------------------
		//	RESPONSE
		//------------------------------------------------------------------------------
		return $oResult;

	}
	private static function Result_Message($Code)
	{
		//------------------------------------------------------------------------------
		//	VARIABLES
		//------------------------------------------------------------------------------
		$oMessage	= include(resource_path("responses/message.php"));
		$oResult	= "";


		//------------------------------------------------------------------------------
		//	FUNCTION
		//------------------------------------------------------------------------------
		if ( array_key_exists($Code, $oMessage) )
		{
			$oResult = $oMessage[$Code]["Message"];
		}
		else
		{
			$oResult = $oMessage[9900]["Message"];
		}


		//------------------------------------------------------------------------------
		//	RESPONSE
		//------------------------------------------------------------------------------
		return $oResult;

	}
}