<?php

namespace App\Helpers;


class FunctionHelper
{
	public static function Query_Where_Display($Display, $Field)
	{
		//------------------------------------------------------------------------------
		//	VARIABLES
		//------------------------------------------------------------------------------
		$oResult 	= [];


		//------------------------------------------------------------------------------
		//	FUNCTION
		//------------------------------------------------------------------------------
		switch ($Display)
		{
			case "all"		: $oResult = ["$Field", ">", "0"]; break;
			case "private"	: $oResult = ["$Field", "=", "1"]; break;
			case "public"	: $oResult = ["$Field", "=", "2"]; break;
			default			: $oResult = ["$Field", "=", "2"]; break;
		}


		//------------------------------------------------------------------------------
		//	RESPONSE
		//------------------------------------------------------------------------------
		return $oResult;

	}
	public static function Query_Where_Status($Status, $Field)
	{
		//------------------------------------------------------------------------------
		//	VARIABLES
		//------------------------------------------------------------------------------
		$oResult 	= [];


		//------------------------------------------------------------------------------
		//	FUNCTION
		//------------------------------------------------------------------------------
		switch($Status)
		{
			case "all"		: $oResult = ["$Field", ">", "0"]; break;
			case "inactive"	: $oResult = ["$Field", "=", "1"]; break;
			case "active"	: $oResult = ["$Field", "=", "2"]; break;
			default			: $oResult = ["$Field", "=", "2"]; break;
		}


		//------------------------------------------------------------------------------
		//	RESPONSE
		//------------------------------------------------------------------------------
		return $oResult;

	}
}